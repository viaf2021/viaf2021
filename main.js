var popupView = document.querySelector(".flower");
var questionView = document.querySelector(".question");

function popUp(num) {
   if (num == 3 || num == 5 || num == 6 || num == 8) {
      fadeInQuest(num);
   } else {
      fadeIn(num);
   }
}

// Switching fade popup page
function fadeIn(num) {
   setData(num)
   if (popupView.classList.contains("m-fadeIn") == false) {
      popupView.classList.add("m-fadeIn");
   } else {
      popupView.classList.replace("m-fadeOut", "m-fadeIn");
   }
}

function fadeOut() {
   stopVideo();
   if (unityEditor != null) {
      unityEditor.SendMessage("Player", "EditSpeed", 70);
   }
   popupView.classList.replace("m-fadeIn", "m-fadeOut");
}

function fadeInQuest(num) {
   setQuest(num, 0)
   if (questionView.classList.contains("m-fadeIn") == false) {
      questionView.classList.add("m-fadeIn");
   } else {
      questionView.classList.replace("m-fadeOut", "m-fadeIn");
   }
}

function fadeOutQuest() {
   if (unityEditor != null) {
      unityEditor.SendMessage("Player", "EditSpeed", 70);
   }
   questionView.classList.replace("m-fadeIn", "m-fadeOut");
}

