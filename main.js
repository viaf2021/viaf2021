function setData(num) {
   var titleKor = document.querySelector(".mv.title.kor");
   var titleEng = document.querySelector(".mv.title.eng");
   var teamKor = document.querySelector(".mv.team.kor");
   var teamEng = document.querySelector(".mv.team.eng");
   var genreKor = document.querySelector(".mv.genre.kor")
   var genreEng = document.querySelector(".mv.genre.eng")
   var duration = document.querySelector(".mv.duration");

   var LoadedVideoId = playInfo[num].PlaySrc;
   player.cueVideoById(LoadedVideoId);

   setImage(num);
   titleKor.innerHTML = playInfo[num].TitleKor;
   titleEng.innerHTML = playInfo[num].TitleEng;
   teamKor.innerHTML = playInfo[num].TeamKor;
   teamEng.innerHTML = playInfo[num].TeamEng;

   const genreStr = playInfo[num].Genre;
   genreKor.innerHTML = genreStr.split(' ')[0];
   genreEng.innerHTML = genreStr.split(' ')[1];

   duration.innerHTML = playInfo[num].Time;
}

function setImage(playNum) {
   var imgPetal1 = document.querySelector(".img-petal1");
   var imgPetal2 = document.querySelector(".img-petal2");
   var imgFlower = document.querySelector(".img-flower");

   var flowerType = "";

   console.log(playNum);

   if (playNum <= 10) {
      flowerType = "rose";
      console.log("극단");
   } else if (10 < playNum && playNum <= 13) {
      flowerType = "dand";
      console.log("청년");
   } else if (13 < playNum) {
      flowerType = "lotus";
      console.log("일반");
   }

   imgPetal1.src = `./Img/${flowerType}_petal01.png`;
   imgPetal2.src = `./Img/${flowerType}_petal02.png`;
   imgFlower.src = `./Img/${flowerType}_flower.png`;
}

var optionContainer = document.querySelector(".quest.selector")

function setQuest(num, questIndex) {
   clearQuest(optionContainer); // 기존 문제 치우기

   var questTitle = document.querySelector(".quest.title");
   var questTrigger = true;
   var questsList = playInfo[num]; //Type: array. 1개~2개 들어있음

   if (questIndex == 1) {
      questTrigger = false;
   }

   if (questsList.Question.length > 1) { //질문이 1개 이상일 때    
      questTitle.innerHTML = questsList.Question[questIndex];

      var ansOptions = questsList.Options[questIndex];
      setAnswers(ansOptions, num, questTrigger);

   } else {
      questTitle.innerHTML = questsList.Question[0];

      var ansOptions = questsList.Options;
      setAnswers(ansOptions, num, false);
   }
}

function setAnswers(options, num, questTrigger) {
   options.forEach((option, idx, _) => {
      var opSel = document.createElement("div");
      opSel.classList.add("quest");
      opSel.classList.add("ans");
      opSel.classList.add(`${idx+1}`);
      optionContainer.appendChild(opSel);
      opSel.innerHTML = option;

      if (questTrigger == true) {
         opSel.addEventListener("click", function () {
            setQuest(num, 1);
         }, false);

      } else {
         opSel.addEventListener("click", function () {
            fadeIn(num);
            fadeOutQuest();
         }, false);
      }

   });
}

function clearQuest(parent) {
   while (parent.firstChild) {
      parent.removeChild(parent.firstChild);
   }
}

function endGuide() {
   var firstGuide = document.querySelector(".eguide01");
   var secondGuide = document.querySelector(".eguide02");

   firstGuide.classList.replace("m-fadeOut", "m-fadeIn");

   setTimeout(() => {
      secondGuide.classList.replace("m-fadeOut", "m-fadeIn");
      setTimeout(() => {
         firstGuide.classList.replace("m-fadeIn", "m-fadeOut");
         secondGuide.classList.replace("m-fadeIn", "m-fadeOut");
      }, 5000);
   }, 2000);
}

function selectEnding() {
   var endingWindow = document.querySelector(".ebutton");
   var endingBtn = document.querySelector(".ending-btn");
   var returnBtn = document.querySelector(".return-btn");

   endingWindow.classList.replace("m-fadeOut", "m-fadeIn");

   endingBtn.addEventListener("click", function () {
      // Hide editor screen and buttons
      container.style.visibility = "hidden";
      endingWindow.style.visibility = "hidden";

      startEnding();
   }, false);

   returnBtn.addEventListener("click", function () {
      console.log("return to start!");
      if (unityEditor != null) {
         unityEditor.SendMessage("Base", "ToStartPosition");
         unityEditor.SendMessage("Player", "EditSpeed", 70);
         endingWindow.classList.replace("m-fadeIn", "m-fadeOut");
      }
   }, false)
}

function startEnding() {
   var containerEl = document.createElement("div");
   var vidEl = document.createElement("video");
   containerEl.appendChild(vidEl);
   containerEl.classList.add("intro-container");
   vidEl.classList.add("intro-mov");
   vidEl.src = "./video/Viaf_outro.mp4";

   document.body.insertBefore(containerEl, container);

   // ContainerEl.style.visibility = "visible";
   vidEl.play();
}
